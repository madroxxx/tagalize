/**
* @Method: returns the localized form of words
* @Param: {string}
* @Retrun: {string}
*/
export namespace getTagalized {
     export function localBatang ( str: any ) : string {
          return str + "-EY";
     }
     export function localIlong ( str: any ) : string {
          return str + "-OY";
     }
     export function localCeb ( str: any ) : string {
          return str + "-IY";
     }
}
