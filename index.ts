/**
* @Method: returns the localized form of words
* @Param: {string}
* @Retrun: {string}
*/
export class getTagalized {
     localBatang ( str: any ) : string {
          return str + "-EY";
     }
     localIlong ( str: any ) : string {
          return str + "-OY";
     }
     localCeb ( str: any ) : string {
          return str + "-IY";
     }
}
